	"ID" INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
	"TRESC" TEXT NOT NULL,
	"NADAWCA_ID" INTEGER REFERENCES user("ID") NOT NULL,
	"ODBIORCA_ID" INTEGER REFERENCES user("ID") NOT NULL,
	"CZY_PRZECZYTANY" INTEGER NOT NULL DEFAULT "0",
	"DATA_WYSLANIA" TEXT NOT NULL
);