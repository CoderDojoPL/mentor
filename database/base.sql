CREATE TABLE "user" (
    "ID" INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    "EMAIL" TEXT NOT NULL,
    "PASSWORD" TEXT NOT NULL,
    "LOGIN" TEXT NOT NULL,
    "LOKALIZACJA" TEXT NOT NULL,
    "CZY_USPIONY" INTEGER NOT NULL DEFAULT (1),
    "CZY_ZDALNY" INTEGER NOT NULL DEFAULT (0),
    "CZY_PEDAGOG" INTEGER NOT NULL DEFAULT (0)
);
CREATE TABLE "dojo" (
    "ID" INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    "LOKALIZACJA" TEXT NOT NULL,
    "NAZWA" TEXT NOT NULL,
    "IL_MENTOROW" INTEGER NOT NULL DEFAULT (0)
);
CREATE TABLE "sl_umiejetnosci" (
    "ID" INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    "NAZWA" TEXT NOT NULL
);
CREATE TABLE "dojo_user" (
	"DOJO_ID" INTEGER REFERENCES dojo("ID") NOT NULL,
	"USER_ID" INTEGER REFERENCES user("ID") NOT NULL
);
CREATE TABLE "user_umiejetnosc" (
	"USER_ID" INTEGER REFERENCES user("ID") NOT NULL,
	"UMIEJETNOSC_ID" INTEGER REFERENCES sl_umiejetnosci("ID") NOT NULL
);
